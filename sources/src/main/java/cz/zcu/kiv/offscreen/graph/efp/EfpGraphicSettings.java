package cz.zcu.kiv.offscreen.graph.efp;

/**
 * 
 * @author Jiri Loudil
 *
 */
public class EfpGraphicSettings {
	private int minInterfaceDiameter, maxInterfaceDiameter;

	public EfpGraphicSettings() {
		
	}

	public int getMinInterfaceDiameter() {
		return minInterfaceDiameter;
	}

	public void setMinInterfaceDiameter(int minInterfaceDiameter) {
		this.minInterfaceDiameter = minInterfaceDiameter;
	}

	public int getMaxInterfaceDiameter() {
		return maxInterfaceDiameter;
	}

	public void setMaxInterfaceDiameter(int maxInterfaceDiameter) {
		this.maxInterfaceDiameter = maxInterfaceDiameter;
	}

}
